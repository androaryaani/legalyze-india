// Helper functions to search legal database
export const searchConstitution = (query: string) => {
  const results = []
  for (const [article, data] of Object.entries(CONSTITUTION_ARTICLES)) {
    if (
      data.title.toLowerCase().includes(query.toLowerCase()) ||
      data.content.toLowerCase().includes(query.toLowerCase()) ||
      data.category.toLowerCase().includes(query.toLowerCase())
    ) {
      results.push({ article: `Article ${article}`, ...data })
    }
  }
  return results
}

export const searchIPC = (query: string) => {
  const results = []
  for (const [section, data] of Object.entries(IPC_SECTIONS)) {
    if (
      data.title.toLowerCase().includes(query.toLowerCase()) ||
      data.category.toLowerCase().includes(query.toLowerCase())
    ) {
      results.push({ section: `IPC Section ${section}`, ...data })
    }
  }
  return results
}

export const searchCrPC = (query: string) => {
  const results = []
  for (const [section, data] of Object.entries(CRPC_SECTIONS)) {
    if (
      data.title.toLowerCase().includes(query.toLowerCase()) ||
      data.content.toLowerCase().includes(query.toLowerCase()) ||
      data.category.toLowerCase().includes(query.toLowerCase())
    ) {
      results.push({ section: `CrPC Section ${section}`, ...data })
    }
  }
  return results
}

export const getLegalReferences = (category: string, keywords: string[]) => {
  const references = []

  // Search Constitution
  keywords.forEach((keyword) => {
    references.push(...searchConstitution(keyword))
  })

  // Search IPC
  keywords.forEach((keyword) => {
    references.push(...searchIPC(keyword))
  })

  // Search CrPC
  keywords.forEach((keyword) => {
    references.push(...searchCrPC(keyword))
  })

  return references.slice(0, 5) // Return top 5 relevant results
}
